/*********************************************************************************************************
** 函数名称: SendRecv_Byte
** 函数功能：一次传输内部的单字节帧发送与接收
** 输入参数: 数据线上发送出的数据
** 输出参数: 数据线上接收到的数据
** 返 回 值：无
*********************************************************************************************************/
extern uint8_t SendRecv_Byte (uint8_t ucData);

/*********************************************************************************************************
** 函数名称: SendRecv_Start
** 函数功能：一次传输起始的单字节帧发送与接收
** 输入参数: 数据线上发送出的数据
** 输出参数: 数据线上接收到的数据
** 返 回 值：无
*********************************************************************************************************/
extern uint8_t SendRecv_Start (uint8_t ucData);

/*********************************************************************************************************
** 函数名称: SendRecv_Stop
** 函数功能：一次传输结束的单字节帧发送与接收
** 输入参数: 数据线上发送出的数据
** 输出参数: 数据线上接收到的数据
** 返 回 值：无
*********************************************************************************************************/
extern uint8_t SendRecv_Stop (uint8_t ucData);














